#include <stdio.h>
#include <stdlib.h>

int i, j;

int main(void)
{
	printf("(A)");
	for (i = 0; i < 10; i++)
	{
		for (j = 0; j <= i; j++)
		{
			printf("%s","*");
		}
		printf("\n");
	}

	//================================================================================================================================
	
	printf("(B)\n");
	for (i = 10; i > 0; i--)
	{
		for (j = 0; j < i; j++)
		{
			printf("%s", "*");
		}
		printf("\n");
	}

	//================================================================================================================================
	
	printf("(C)\n");
	for (i = 0; i < 10; i++)
	{	
		for (j = 0; j < 10; j++)
		{
			if (j >= i)
			{
				printf("%s", "*");
			}
			else
			{
				printf(" ");
			}
		}
		printf("\n");
	}

	//================================================================================================================================
	
	printf("(D)\n");
	for (i = 0; i < 10; i++)
	{
		for (j = 0; j < 10; j++)
		{
			if (i>=j)
			{
				printf("%s", "*");
			}
			else
			{
				printf(" ");
			}
		}
		printf("\n");
	}

	//================================================================================================================================
	system("pause");
}
